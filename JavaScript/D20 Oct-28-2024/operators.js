// 4. Bitwise operators
/**
 * & (Bitwise and)
 * | (Bitwise or)
 * ^ (Bitwise xor)
 * ~ (Bitwise not)
 * << (Left shift)
 * >> (Right shift)
 * >>> (Zero fill right shift)
 */
//  5 covert into binary with toString(2)

console.log((4).toString(2)); // 101

// use of &
console.log(5 & 1); // 1
//  0101 & 0001 = 0001

// use of |
console.log(5 | 3); // 7
//  0101 | 0011 = 0111

// use of ^
console.log(7 ^ 2); // 5
//  0111 ^ 0010 = 0101

// use of ~
console.log(~5); // -6
//  ~0101 = 1010

// use of <<
console.log(-6 << 2); // -24
//  1010 << 2 = 101000

// use of >>
console.log(5 >> 2); // 1
//  0101 >> 2 = 0001

// use of >>>
console.log(5 >>> 1); // 2
//  0101 >>> 1 = 0010

